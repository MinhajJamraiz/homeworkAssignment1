public class Parcel {
    // Attributes
    String parcelID;
    double weight;
    String destination;
    int priorityLevel;
    
    // Constructor
    Parcel(String parcelID, double weight, String destination, int priorityLevel) {
        this.parcelID = parcelID;
        this.weight = weight;
        this.destination = destination;
        this.priorityLevel = priorityLevel;
    }

    
}
